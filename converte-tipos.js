// Entrada de string
let numeroString = "123";
console.log('Número inicial: ', typeof numeroString); // Saída: string

// Convertendo para número inteiro
let numeroInteiro = parseInt(numeroString);
console.log('1a. conversao para inteiro: ', typeof numeroInteiro); // Saída: number

// Convertendo para número decimal (float)
let numeroDecimalString = "123.45";
let numeroDecimal = parseFloat(numeroDecimalString);
console.log('2a. conversao para decimal: ', typeof numeroDecimal); // Saída: number
